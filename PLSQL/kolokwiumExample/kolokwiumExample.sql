CREATE OR REPLACE PROCEDURE ex3(categoryName VARCHAR)
AS BEGIN
    IF (SELECT ID_KATEGORIA FROM KATEGORIA WHERE KATEGORIA=categoryName) IS NULL THEN
        INSERT INTO KATEGORIA VALUES ((SELECT MAX(ID_KATEGORIA) FROM KATEGORIA)+1, categoryName);
    end if;
    UPDATE PRODUKT SET ID_KATEGORIA=(SELECT KATEGORIA.ID_KATEGORIA FROM KATEGORIA WHERE KATEGORIA=categoryName)
    WHERE ID_KATEGORIA IS NULL;
end;